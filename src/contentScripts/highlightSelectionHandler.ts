function getSelectionRect(): DOMRect | null {
  const selection = window.getSelection()
  if (!selection || selection.rangeCount === 0)
    return null
  const range = selection.getRangeAt(0)
  if (range.collapsed)
    return null
  return range.getBoundingClientRect()
}

